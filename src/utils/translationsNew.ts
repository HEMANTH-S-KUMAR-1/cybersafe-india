// Simple translation function
export const t = (key: string, language: string = 'en'): string => {
  const translation = translations[key];
  if (!translation) {
    // If no translation found, return the key itself for display
    return key;
  }
  
  return translation[language as keyof TranslationKey] || translation.en || key;
};

// Get current language from localStorage or default to English
export const getCurrentLanguage = (): string => {
  return localStorage.getItem('cybersafe-language') || 'en';
};

// Set current language and save to localStorage
export const setLanguage = (language: string): void => {
  localStorage.setItem('cybersafe-language', language);
  
  // Dispatch global event for language change
  const event = new CustomEvent('languageChanged', {
    detail: { language }
  });
  window.dispatchEvent(event);
};

// Check if language is RTL (right-to-left)
export const isRTLLanguage = (language: string): boolean => {
  const rtlLanguages = ['ar', 'he', 'fa', 'ur'];
  return rtlLanguages.includes(language);
};

// Get language direction
export const getLanguageDirection = (language: string): 'ltr' | 'rtl' => {
  return isRTLLanguage(language) ? 'rtl' : 'ltr';
};
